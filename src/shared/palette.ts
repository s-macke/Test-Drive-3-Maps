/**
 * Look up VGA palette slot `idx` and return its RGB as 0..1 floats.
 * `idx` is a byte (0..255); out-of-range indices read uninitialised slots.
 */
export function paletteColor(idx: number): ColorRGB {
    return new ColorRGB(
        pal[idx * 3 + 0] / 256.,
        pal[idx * 3 + 1] / 256.,
        pal[idx * 3 + 2] / 256.,
    );
}
